print("1- Añadir una nueva tarea")
print("2- Editar una tarea")
print("3- Ver todas las tareas")
print("0- Salir")

opcion = input("Qué opcion? ")

if opcion == "1":
    print("Añadir")
elif opcion == "2":
    print("Editar")
elif opcion == "3":
    print("Ver")
elif opcion == "0":
    print("Salir")
else:
    print("Opción no válida")
